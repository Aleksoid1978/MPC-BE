    if ((bsDiffType[0] || bsDiffType[1]) &&
       !(!allowDiffTimeBackFlag && bsDiffType[0]) &&
       !(!bsDataPairXXX || (bsDataPairXXX && bsDiffType[1])))
       Skip_SB(                                                 "bsDiffTimeDirection[0]");

